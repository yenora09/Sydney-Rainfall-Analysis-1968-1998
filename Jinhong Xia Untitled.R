period1 <- c(rep(103.0, 120)) 
period2 <- c(rep(68.3, 120))  
period3 <- c(rep(91.5, 72)) 


rainfall_data <- data.frame(
  Rainfall = c(period1, period2, period3),
  Period = factor(c(rep("1968-1977", 120), rep("1978-1987", 120), rep("1988-1993", 72)))
)


k_test <- kruskal.test(Rainfall ~ Period, data = rainfall_data)


print(k_test)


if(k_test$p.value < 0.05) {
  library(FSA)
  dunnTest(Rainfall ~ Period, data = rainfall_data, method = "bh")
